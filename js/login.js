$(document).ready(function(){
	//window.alert('hey!')
	$('[name="submit"]').click(function(e){
	    e.preventDefault();
	    login = $('[name="login"]').val();
	    pass = $('[name="password"]').val();
	    
	    $.ajax({
	        type: 'post',
	        url: 'login',
	        data: {login:login,password:pass},
	        success: function(data){
	            console.log(data)
	            if(data.msg==0){
	            	$(".alert-error").css("display","block")
	            }else{

	            }
	        },
	        error: function(jqxhr){
	            console.log(jqxhr)
	        }
	    })
	})

});